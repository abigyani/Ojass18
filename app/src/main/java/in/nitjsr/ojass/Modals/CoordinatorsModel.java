package in.nitjsr.ojass.Modals;


public class CoordinatorsModel {


    String name;
    String phone;


    public CoordinatorsModel(String name, String phone) {

        this.name = name;
        this.phone=phone;

    }
    public CoordinatorsModel()
    {

    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }


}
